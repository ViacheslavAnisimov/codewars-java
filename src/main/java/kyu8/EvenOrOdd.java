package kyu8;

public class EvenOrOdd {
    public static String even_or_odd(int number) {
// Create a function that takes an integer as an argument and returns "Even" for even numbers
// or "Odd" for odd numbers.

        if (number % 2 == 0) {
            return "Even";
        } else {
            return "Odd";
        }
    }
}
